//java program to understand data types
public class DataTypes
{
public static void main(String[] args)
{
Boolean one = false; //Stores true or false values
byte a = 10; 		//Stores whole numbers from -128 to 127
byte b = -20;
short s = 10000;	//Stores whole numbers from -32,768 to 32,767
short r = -5000;
int c = 100000; 	//Stores whole numbers from -2,147,483,648 to 2,147,483,647
int d = -200000;
long e = 100000L; 	//Stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
long f = -200000L;
float f1 = 234.5f;	//Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
double d1 = 12.3;	//Stores fractional numbers. Sufficient for storing 15 decimal digits
char letterA = 'A';	//Stores a single character/letter or ASCII values
System.out.println("boolean: "+one);
System.out.println("byte: "+a);
System.out.println("byte: "+b);
System.out.println("short: "+s);
System.out.println("short: "+r);
System.out.println("int: "+c);
System.out.println("int: "+d);
System.out.println("long: "+e);
System.out.println("long: "+f);
System.out.println("float: "+f1);
System.out.println("double: "+d1);
System.out.println("char: "+letterA);
}
}

  